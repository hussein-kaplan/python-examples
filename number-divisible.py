# برنامج بايثون للعثور على أرقام قابلة للقسمة على رقم آخر

#  اخذ قائمة الأرقام
my_list = [12, 65, 54, 39, 102, 339, 221,]

# استخدم وظيفة مجهولة للتصفية
result = list(filter(lambda x: (x % 13 == 0), my_list))

# عرض النتائج
print("الأعداد القابلة للقسمة على ١٣ هي",result)