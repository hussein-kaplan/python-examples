# برنامج بايثون للتحقق مما إذا كان الرقم فرديًا أم زوجيًا

# يكون الرقم عدد زوجي إذا كانت  تقبل القسمة على 2 و الباقي  0.
# إذا كان الباقي 1 ، فهو رقم فردي.
num = int(input("اكتب الرقم: "))
if (num % 2) == 0:
   print("{0} هو زوجي".format(num))
else:
   print("{0} هو فردي".format(num))