# --- برنامج بايثون لخلط أوراق اللعب عشوائيًا

# --- (random)في هذا البرنامج ، ستتعلم خلط مجموعة أوراق اللعب باستخدام وحدة عشوائية.


# --- استدعاء المكتبات المطلوبة
import itertools, random

# ---  صنع مجموعة من البطاقات

deck = list(itertools.product(range(1,14),['سيناك','كوبة','دينار','ماجا']))

# --- خلط الاوراق
random.shuffle(deck)

# --- اخراج خمس بطاقات
print("لقد حصلت :")
for i in range(5):
   print(deck[i][0], "من", deck[i][1])
